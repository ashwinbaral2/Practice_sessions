# Q1: Write a function that returns the sum of all numbers in the list.
# Example:
# Input: [1, 2, 3]
# Output: 6

def sum_of_list(nums):
    total = 0
    for num in nums:
        total += num
    return total

print(sum_of_list([1, 2, 3]))
#output: 6



