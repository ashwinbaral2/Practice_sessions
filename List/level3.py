# Q1: Given a list, print the first and last elements.
# Example:
# Input: [10, 20, 30, 40]
# Output:
# First element: 10
# Last element: 40
def print_first_last(nums):
    if len(nums) == 0:
        print("List is empty")
        return
    print("First element:", nums[0])
    print("Last element:", nums[-1])
print_first_last([10, 20, 30, 40])
#output:
#First element: 10
#Last element: 40



